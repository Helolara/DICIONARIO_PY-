# Criando o dicionário
meu_dicionario = {
    "chave1": 10,
    "chave2": 20,
    "chave3": 30
}

# Verificando se a chave "chave2" existe no dicionário
chave = "chave2"
if chave in meu_dicionario:
    print(f"A chave '{chave}' existe no dicionário.")
else:
    print(f"A chave '{chave}' não existe no dicionário.")